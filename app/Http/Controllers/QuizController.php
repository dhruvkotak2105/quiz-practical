    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validatedData = $request->validate([
            'quiz_name' => 'required|string',
            'questions.*' => 'required|exists:questions,id',
        ]);
    
        $quiz = Quiz::create([
            'name' => $validatedData['quiz_name'],
        ]);
    
        $quiz->questions()->attach($validatedData['questions']);
    
        return redirect()->route('home')->with('success', 'Quiz created successfully.');
    }

